package com.bean;

public class Student {
    String xshao;
    String password;
    String name;
    int xuefen;

    public int getXuefen() {
        return xuefen;
    }

    public void setXuefen(int xuefen) {
        this.xuefen = xuefen;
    }

    public String getName() {

        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {

        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getXshao() {

        return xshao;
    }

    public void setXshao(String xshao) {
        this.xshao = xshao;
    }
}
